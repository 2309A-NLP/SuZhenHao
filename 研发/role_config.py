DEFAULT_ROLE_CODE = "psychologist"


def get_role_config(role_code: str) -> Dict[str, str]:
    for item in ROLE_CONFIGS:
        if item["role_code"] == role_code:
            return item
    for item in ROLE_CONFIGS:
        if item["role_code"] == DEFAULT_ROLE_CODE:
            return item
    return ROLE_CONFIGS[0]


def list_roles() -> List[Dict[str, str]]:
    return [dict(item) for item in ROLE_CONFIGS]


def get_role_prompt(role_code: str) -> str:
    config = get_role_config(role_code)
    return ROLE_PROMPTS.get(role_code, ROLE_PROMPTS[config["role_code"]])
